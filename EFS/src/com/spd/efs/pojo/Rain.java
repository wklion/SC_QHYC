package com.spd.efs.pojo;

public class Rain {
	
	
	    public String stationNum;
	    public String stationName;
	    public double rain_avg;
	    public double rain_anomaly;
	    
	    
	    
		public String getStationNum() {
			return stationNum;
		}
		public void setStationNum(String stationNum) {
			this.stationNum = stationNum;
		}
		public String getStationName() {
			return stationName;
		}
		public void setStationName(String stationName) {
			this.stationName = stationName;
		}
		public double getRain_avg() {
			return rain_avg;
		}
		public void setRain_avg(double rainAvg) {
			rain_avg = rainAvg;
		}
		public double getRain_anomaly() {
			return rain_anomaly;
		}
		public void setRain_anomaly(double rainAnomaly) {
			rain_anomaly = rainAnomaly;
		}
	    
	    
	    
	    
	    

}
