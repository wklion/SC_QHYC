package com.spd.pojo;

/**
 * 用于临时计算的一个计数类
 * @author Administrator
 *
 */
public class ItemCnt {

	private int cnt;
	
	private double sum;

	public int getCnt() {
		return cnt;
	}

	public void setCnt(int cnt) {
		this.cnt = cnt;
	}

	public double getSum() {
		return sum;
	}

	public void setSum(double sum) {
		this.sum = sum;
	}
	
}
