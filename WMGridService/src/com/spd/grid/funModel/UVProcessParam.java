package com.spd.grid.funModel;
/**
 * @author 杠上花
 */
public class UVProcessParam {
	private String level;
	private String period;
	private String resDate;
	private String tempDir;
	private String derfUVDir;
	private String uvDir;
	
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getResDate() {
		return resDate;
	}
	public void setResDate(String resDate) {
		this.resDate = resDate;
	}
	public String getTempDir() {
		return tempDir;
	}
	public void setTempDir(String tempDir) {
		this.tempDir = tempDir;
	}
	public String getDerfUVDir() {
		return derfUVDir;
	}
	public void setDerfUVDir(String derfUVDir) {
		this.derfUVDir = derfUVDir;
	}
	public String getUvDir() {
		return uvDir;
	}
	public void setUvDir(String uvDir) {
		this.uvDir = uvDir;
	}
	
}
