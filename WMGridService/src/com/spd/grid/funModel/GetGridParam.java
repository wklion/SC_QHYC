package com.spd.grid.funModel;

public class GetGridParam {
	private String file;
	private String avgFile;
	private int hourspan;
	private String valTypeID;
	private String elementID;
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	
	public String getAvgFile() {
        return avgFile;
    }
    public void setAvgFile(String avgFile) {
        this.avgFile = avgFile;
    }
    public int getHourspan() {
		return hourspan;
	}
	public void setHourspan(int hourspan) {
		this.hourspan = hourspan;
	}
	public String getValTypeID() {
		return valTypeID;
	}
	public void setValTypeID(String valTypeID) {
		this.valTypeID = valTypeID;
	}
	public String getElementID() {
		return elementID;
	}
	public void setElementID(String elementID) {
		this.elementID = elementID;
	}
	
}
